func TestTimerCreate(t *testing.T) {
	mcpServer := mcpServerMock(t, http.StatusCreated, []byte(`{"timer":{"id":123}}`))

	request := &toolRequest{
		JSONRPC: mcp.JSONRPC_VERSION,
		ID:      1,
		CallToolRequest: mcp.CallToolRequest{
			Request: mcp.Request{
				Method: string(mcp.MethodToolsCall),
			},
		},
	}
	request.Params.Name = twprojects.MethodTimerCreate.String()
	request.Params.Arguments = map[string]any{
		"description":         "Example timer description",
		"billable":            true,
		"running":             true,
		"seconds":             float64(3600), // 1 hour
		"stop_running_timers": true,
		"project_id":          float64(123),
		"task_id":             float64(456),
	}

	encodedRequest, err := json.Marshal(request)
	if err != nil {
		t.Fatalf("failed to encode request: %v", err)
	}

	checkMessage(t, mcpServer.HandleMessage(context.Background(), encodedRequest))
}

